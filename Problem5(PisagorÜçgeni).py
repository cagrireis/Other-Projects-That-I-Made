def pisagor():
    liste1 = []
    for element1 in range(1,101):
        for element2 in range(1,101):
            Z = (element1 ** 2 + element2 ** 2) ** 0.5
            if (Z % int(Z) == 0):
                liste1.append(("Z =", Z, "a =", element1, "b =", element2))
    return ("1 ila 100 arasındaki üçgen üçlüleri:", liste1)
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
print(pisagor())